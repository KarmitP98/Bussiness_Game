    private void checkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkActionPerformed
       String n,t;     //n is used for your name and t is used for the format of the height you want to select
       float h,w;   //h=height w=weight
       n=(String) name.getText();  //Taking the imput from the user
       h=Float.parseFloat(height.getText());
       t=(String) type.getSelectedItem();
       if(t=="meters")  //if and else used to display the wieght into imperial if feet/inch 
       {                //are selected and to metric if cm/m are selected
           w=h*h*25;
           display.setText(n+"'s ideal weight is "+String.format("%.2f",w)+"kg");
       }
       else if(t=="centimeters")
       {
           h=h/100;
           w=h*h*25;
           display.setText(n+"'s ideal weight is "+String.format("%.2f",w)+"kg");
        }
       else if(t=="feet")
       {
           h=h*12;
           w=h*h*25/703;
           display.setText(n+"'s ideal weight is "+String.format("%.2f",w)+"lbs.");
       }
       else{
           w=h*h*25/703;
           display.setText(n+"'s ideal weight is "+String.format("%.2f",w)+"lbs.");
       }
       happy.setText("Happy :))");     //Small message from me
       not.setText("Not Happy ;(( B#&%!#@@^);");
       //NOTE:The prompt where you have to select how many times do oyu eat in a day does not affect your program it is just added to add some variety on screen
    }//GEN-LAST:event_checkActionPerformed

