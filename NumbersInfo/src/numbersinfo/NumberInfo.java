    private void removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeActionPerformed
        int num;
        num=(int) Integer.parseInt(number.getText());        
        numero.remove(num);        
        number.setText(null);
        
       checker();
    }//GEN-LAST:event_removeActionPerformed

    private void listActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_listActionPerformed
        int i;      
        display.setText(null);
        for(i=0;i<numero.size();i++)
        {
          display.append(i+1+". "+numero.get(i)+"\n");
        }
       
        checker();
    }//GEN-LAST:event_listActionPerformed

    private void sumaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sumaActionPerformed
       int sum=0,i;
       for(i=0;i<numero.size();i++)
       {
           sum=sum+(int)numero.get(i);
       }   
       
        display.append("\n\nSum: "+sum);
       
        checker();
    }//GEN-LAST:event_sumaActionPerformed

    private void sumeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sumeActionPerformed
       int sum=0,i;
       for(i=0;i<numero.size();i++)
       {
           if((int)numero.get(i)%2==0)
           {
               sum+=(int)numero.get(i);
           }
       }
       
       display.append("\n\nEven Sum: "+sum);
       
       checker();
    }//GEN-LAST:event_sumeActionPerformed

    private void sumoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sumoActionPerformed
        int sum=0,i;
       for(i=0;i<numero.size();i++)
       {
           if((int)numero.get(i)%2==1)
           {
               sum+=(int)numero.get(i);
           }
       }
       
       display.append("\n\nOdd Sum: "+sum);
       
       checker();
    }//GEN-LAST:event_sumoActionPerformed

    private void rangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rangeActionPerformed
        int max,min,range;
        max=(int) Collections.max(numero);
        min=(int) Collections.min(numero);
        range=max-min;
        display.append("\n\nRange: "+range);
      
        checker();
    }//GEN-LAST:event_rangeActionPerformed

    private void meanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_meanActionPerformed
        int sum=0,i;
        int m;
       for(i=0;i<numero.size();i++)
       {
           sum=sum+(int)numero.get(i);
       }   
       
        m=(int) sum/numero.size();
        display.append("\n\nMean: "+m);
        
        checker();
    }//GEN-LAST:event_meanActionPerformed

    private void doneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doneActionPerformed
       
        
        try {
            JOptionPane.showMessageDialog(rootPane,"Thankyou!!!!", null,JOptionPane.PLAIN_MESSAGE);
            Thread.sleep(1000);
        } catch (InterruptedException ex) {
            Logger.getLogger(NumberInfo.class.getName()).log(Level.SEVERE, null, ex);
        }
        System.exit(0);
    }//GEN-LAST:event_doneActionPerformed

    private void numberKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_numberKeyPressed
        if (evt.getKeyCode()==KeyEvent.VK_ENTER){            
       int num;
       num= Integer.parseInt(number.getText());
       numero.add(num);
       number.setText(null);
       
        }
       
        
       checker();
    }//GEN-LAST:event_numberKeyPressed

    private void restartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_restartActionPerformed
        try {
            Thread.sleep(10000);
        } catch (InterruptedException ex) {
            Logger.getLogger(NumberInfo.class.getName()).log(Level.SEVERE, null, ex);
        }
        numero.clear();
        display.setText(null);
        checker();
    }//GEN-LAST:event_restartActionPerformed
    
