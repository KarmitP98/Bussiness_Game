    private ArrayList numero=new ArrayList();
  
    
    public void checker(){
        int s=numero.size();
        if(s==0)
        {
            remove.setEnabled(false);
            list.setEnabled(false);
            suma.setEnabled(false);
            sume.setEnabled(false);
            sumo.setEnabled(false);
            range.setEnabled(false);
            mean.setEnabled(false);
        }
        else if(s==1)
        {
            remove.setEnabled(true);
            list.setEnabled(true);
        }
        else{
            remove.setEnabled(true);
            list.setEnabled(true);
            suma.setEnabled(true);
            sume.setEnabled(true);
            sumo.setEnabled(true);
            range.setEnabled(true);
            mean.setEnabled(true);
        }
        size.setText("Array size: "+s);
    }
    
