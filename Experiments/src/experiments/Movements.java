    private void butMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_butMouseClicked
        if (move == 1) {
            move = 0;
        } else {
            move = 1;
        }

        t = new Timer(10, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent ae) {
                if(xVar==0 && yVar ==0 && dir == 4 || dir == 1)
                {
                    dir=1;
                    getVar(dir);
                }
                else if(xVar==screen.getWidth() - disp.getWidth() && yVar ==0 && dir == 1)
                {
                    dir=2;
                    getVar(dir);
                }
                else if(xVar==screen.getWidth() - disp.getWidth() && yVar ==screen.getHeight() - disp.getHeight() && dir == 2)
                {
                    dir=3;
                    getVar(dir);
                }
                else if(xVar==0 && yVar ==screen.getHeight() - disp.getHeight() && dir == 3)
                {
                    dir=4;
                    getVar(dir);
                }
                
                disp.setLocation(xVar,yVar);
                xVar +=chxVar;
                yVar+=chyVar;
                
            }
        });
        t.start();
    }//GEN-LAST:event_butMouseClicked

    
    public void getVar(int d)
    {
        switch(d)
        {
            case 1: xVar = 0; yVar = 0; chxVar= 1; chyVar = 0; break;
            case 2: xVar = screen.getWidth() - disp.getWidth(); yVar = 0; chxVar = 0; chyVar = 1; break;
            case 3: xVar = screen.getWidth() - disp.getWidth(); yVar = screen.getHeight() - disp.getHeight(); chxVar = -1; chyVar =0; break;
            case 4: xVar = 0; yVar = screen.getHeight() - disp.getHeight(); chxVar = 0; chyVar = -1; break;    
        }
    }
    
