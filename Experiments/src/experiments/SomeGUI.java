    public void buttons() {
        bc.add(Button1);
        bc.add(Button2);
        tg[0] = Button1;
        tg[1] = Button2;

        for (i = 0; i < 2; i++) {
            tg[i].addMouseListener(new MouseListener() {

                @Override
                public void mouseClicked(MouseEvent me) {

                    JToggleButton jt = (JToggleButton) me.getSource();
                    int j = 0;

                    for (j = 0; j < 2; j++) {
                        if (tg[j] == jt) {
                            checkText(j);
                        }
                    }
                }

                @Override
                public void mousePressed(MouseEvent me) {
                }

                @Override
                public void mouseReleased(MouseEvent me) {
                }

                @Override
                public void mouseEntered(MouseEvent me) {
                }

                @Override
                public void mouseExited(MouseEvent me) {
                }
            });
        }
    }

    public void checkText(int index)
    {
        if(Display.getText()==tg[index].getText())
        {
            tg[index].setSelected(true);
            Display.setText(null);
        }
        else{
            changeText(index);
        }
    }
    
    public void changeText(int index) {
        Display.setText(tg[index].getText());
    }

