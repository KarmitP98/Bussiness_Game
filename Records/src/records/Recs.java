    private void addActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_addActionPerformed
    {//GEN-HEADEREND:event_addActionPerformed
        id=idn.getText();
        salary=sal.getText();
        names=name.getText();
        if(idn.getText()==null)
          {
            id="NA";
          }
        if(salary==null)
          {
            salary="NA";
          }
        if(names==null)
          {
            names="NA";
          }
            
        info in=new info(id,salary,names);
        listo.add(in);
        idn.setText(null);
        name.setText(null);
        sal.setText(null);
    }//GEN-LAST:event_addActionPerformed

    private void listActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_listActionPerformed
    {//GEN-HEADEREND:event_listActionPerformed
      display.setText(" "); 
      for(x=0;x<listo.size();x++)
          {
            display.append(x+". \n\nId: "+listo.get(x).id+"\nName: "+listo.get(x).names+"\nSalary: $"+listo.get(x).salary);
          }
    }//GEN-LAST:event_listActionPerformed

    private void removeActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_removeActionPerformed
    {//GEN-HEADEREND:event_removeActionPerformed
        id=idn.getText();
        for(x=0;x<listo.size();x++)
          {
            if(id==listo.get(x).id)
              {
                listo.remove(x);                
              }
              
          }
    }//GEN-LAST:event_removeActionPerformed
 
